CREATE DATABASE IF NOT EXISTS practica CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE practica;

CREATE TABLE IF NOT EXISTS US_USUARIOS (
    ID_USUARIO INT NOT NULL AUTO_INCREMENT,
    TX_EMAIL VARCHAR(100) NOT NULL,
    TX_PASSWORD VARCHAR(100) NOT NULL,
    TX_ROLE VARCHAR(20) NOT NULL,
    F_ALTA DATE NOT NULL,
    PRIMARY KEY (ID_USUARIO),
    UNIQUE KEY UK_US_USUARIOS_TX_EMAIL (TX_EMAIL)
);

CREATE TABLE IF NOT EXISTS EM_EMPLEADOS (
    ID_EMPLEADO INT NOT NULL AUTO_INCREMENT,
    TX_NIF VARCHAR(9) NOT NULL UNIQUE,
    TX_NOMBRE VARCHAR(30) NOT NULL,
    TX_APELLIDO1 VARCHAR(40) NOT NULL,
    TX_APELLIDO2 VARCHAR(40) NOT NULL,
    F_NACIMIENTO DATE NOT NULL,
    N_TELEFONO1 VARCHAR(12) NOT NULL,
    N_TELEFONO2 VARCHAR(12) NOT NULL,
    TX_EMAIL VARCHAR(40) NOT NULL,
    F_ALTA DATE NOT NULL,
    F_BAJA DATE NULL,
    CX_EDOCIVIL VARCHAR(1) NOT NULL,
    B_FORMACIONU VARCHAR(1) NOT NULL,
    PRIMARY KEY (ID_EMPLEADO)
);

CREATE TABLE IF NOT EXISTS PR_PROYECTOS (
    ID_PROYECTO INT NOT NULL AUTO_INCREMENT,
    TX_DESCRIPCION VARCHAR(125) NOT NULL,
    F_INICIO DATE NOT NULL,
    F_FIN DATE NULL,
    F_BAJA DATE NULL,
    TX_LUGAR VARCHAR(30) NOT NULL,
    TX_OBSERVACIONES VARCHAR(300) NULL,
    PRIMARY KEY (ID_PROYECTO)
);

CREATE TABLE IF NOT EXISTS PR_EMPLEADOS_PROYECTO (
    ID_PROYECTO INT NOT NULL,
    ID_EMPLEADO INT NOT NULL,
    F_ALTA DATE NOT NULL,
    PRIMARY KEY (ID_PROYECTO, ID_EMPLEADO),
    FOREIGN KEY (ID_PROYECTO) REFERENCES PR_PROYECTOS(ID_PROYECTO),
    FOREIGN KEY (ID_EMPLEADO) REFERENCES EM_EMPLEADOS(ID_EMPLEADO)
);

